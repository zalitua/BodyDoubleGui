package BodyDoublerOO;


/**Person is an abstract class incorporates fields that are common to any subclasses
 * user class such as Admin or Member
 * @author zalit
 */
abstract class Person {
    private String firstName;
    private String lastName;
    private String userID;
    private String password;
    private String email;

    public Person(String userID, String password, String firstName, String lastName, String email) {
        this.userID = userID;
        this.password = password;
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getUserID() {
        return userID;
    }

    public void setUserID(String userID) {
        this.userID = userID;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
